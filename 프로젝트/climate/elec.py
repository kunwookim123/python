def calc_solar_yield(area_m2=3.3058, efficiency=0.20, PSH=4.0, PR=0.8):
    """태양광 하루/연간 발전량 계산"""
    rated_kw = area_m2 * efficiency
    daily_kwh = rated_kw * PSH * PR
    yearly_kwh = daily_kwh * 365
    return daily_kwh, yearly_kwh


